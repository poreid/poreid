    void updateState() {
        switch (++step) {
            case 1:
            case 2:
            case 4:
            case 5:
                status.setText(bundle.getString("dialog.action.step"+step));
                jProgressBar1.setValue(step*20);
                break;
            case 3:
                status.setText(MessageFormat.format(bundle.getString("dialog.action.step"+step),pinLabel));
                jProgressBar1.setValue(step*20);
                break;
            default:
                CardLayout cardLayout = (CardLayout)getContentPane().getLayout();
                cardLayout.show(getContentPane(), "card3");
                setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);
                break;
        }
    }

